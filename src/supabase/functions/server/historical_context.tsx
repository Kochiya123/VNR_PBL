export const getRelevantContext = (query: string): string => {
  // Simple keyword matching for RAG
  const lowerQuery = query.toLowerCase();
  const sections = historicalContext.split('\n##');
  
  // Keywords to search for
  const keywords = lowerQuery.split(' ').filter(word => word.length > 3);
  
  const relevantSections = sections.filter(section => {
    const lowerSection = section.toLowerCase();
    return keywords.some(keyword => lowerSection.includes(keyword));
  });

  if (relevantSections.length > 0) {
    return relevantSections.join('\n##');
  }

  return historicalContext; // Return full context if no specific match
};
